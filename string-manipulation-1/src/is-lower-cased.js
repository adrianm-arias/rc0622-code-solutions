// change the string to lowercase
// compare it with the original string
// if true it is uppercase
// if false it is not uppercased
// return correct boolean statement

function isLowerCased(word) {
  var lowerCase = word.toLowerCase();
  if (word === lowerCase) {
    return true;
  }
  return false;
}

