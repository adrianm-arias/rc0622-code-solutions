// change the string to uppercase
// compare it with the original string
// if true it is uppercase
// if false it is not uppercased
// return correct boolean statement

function isUpperCased(word) {
  var upperCase = word.toUpperCase();
  if (word === upperCase) {
    return true;
  }
  return false;
}

